    //Merge key parts
    template <int T>
    bitset<2*T> mergeTwo(bitset<T> a,bitset<T> b) {
        bitset<2*T> key;

        for(int i = 0; i < 2*T; i++) {
            if(i < T) {
                key[i] = b[i];
            } else
                key[i] = a[i-T];
        }

        return key;

    }




