    //Circular Shift
    template <int T>
    bitset<T> circularShift(bitset<T> a,int num)
    {

        int overflow[num];
        for(int i = 0; i < num; i++)
        {
            overflow[i] = a[T-1-i];
        }

        a = a << num;
        int count1 = 0;
        for(int i = num-1; i >= 0; i--)
        {
            a[i] = overflow[count1++];
        }
        return a;
    }

    template <int T>
    bitset<T/2> firstHalf(bitset<T> a) {
        bitset<T/2> first;
        int count = 0;
        for (int i = T/2; i < T; i++) {
            first[count++] = a[i];
        }
        return first;
    }
    template <int T>
    bitset<T/2> secondHalf(bitset<T> a) {
        bitset<T/2> second;
        int count = 0;
        for (int i = 0; i < T/2; i++) {
            second[count++] = a[i];
        }
        return second;
    }



